async def seed():
    engine = create_async_engine(DATABASE_URL, echo=True)
    async_session = async_sessionmaker(engine, class_=AsyncSession, expire_on_commit=False)

    async with engine.begin() as conn:
        from app.models import Base
        await conn.run_sync(Base.metadata.create_all)

    async with async_session() as db:
        # Insert seiyuu
        for s in SEIYUU:
            await db.execute(
                text(
                    "INSERT OR IGNORE INTO seiyuu (name_zh, name_ja, name_romaji, gender, birth_date, blood_type, height_cm, agency, debut_year) "
                    "VALUES (:zh, :ja, :romaji, :gender, :bd, :bt, :h, :agency, :dy)"
                ),
                {"zh": s[0], "ja": s[1], "romaji": s[2], "gender": s[3], "bd": s[4], "bt": s[5], "h": s[6], "agency": s[7], "dy": s[8]},
            )

        # Insert works
        for w in WORKS:
            await db.execute(
                text(
                    "INSERT OR IGNORE INTO work (title_zh, title_ja, type, premiere_year, episodes, studio) "
                    "VALUES (:zh, :ja, :type, :year, :eps, :studio)"
                ),
                {"zh": w[0], "ja": w[1], "type": w[2], "year": w[3], "eps": w[4], "studio": w[5]},
            )

        await db.commit()

        # Insert roles
        for (si, wi, char) in ROLES:
            await db.execute(
                text(
                    "INSERT OR IGNORE INTO role (seiyuu_id, work_id, character_name) "
                    "VALUES (:sid, :wid, :char)"
                ),
                {"sid": si + 1, "wid": wi + 1, "char": char},
            )

        await db.commit()
        print(f"Seeded {len(SEIYUU)} seiyuu, {len(WORKS)} works, {len(ROLES)} roles.")

    await engine.dispose()


if __name__ == "__main__":
    asyncio.run(seed())
